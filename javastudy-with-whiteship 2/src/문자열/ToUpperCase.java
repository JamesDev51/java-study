package 문자열;

public class ToUpperCase {
      public static void main(String[] args) {
            String s = "Hi, My name is Minseok Jeong.";

            System.out.println(s); //Hi, My name is Minseok Jeong.
            System.out.println(s.toUpperCase()); //HI, MY NAME IS MINSEOK JEONG.
      }
}
