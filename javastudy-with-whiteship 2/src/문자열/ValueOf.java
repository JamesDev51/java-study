package 문자열;

public class ValueOf {
      public static void main(String[] args) {
            int i =123123123;
            long l = 3L;
            char c = '1';

            System.out.println(String.valueOf(i)); //123123123
            System.out.println(String.valueOf(l)); //3
            System.out.println(String.valueOf(c)); //1
      }
}
