package 문자열;

public class ReplaceFirst {
      public static void main(String[] args) {
            String  s="AmomA";
            System.out.println(s.replaceFirst("A","Super")); //SupermomA

            String s2="momA";
            System.out.println(s2.replaceFirst("A","AndDad")); //momAndDad

      }
}
