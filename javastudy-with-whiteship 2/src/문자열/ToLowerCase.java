package 문자열;

public class ToLowerCase {
      public static void main(String[] args) {
            String s = "Hi, My name is Minseok Jeong.";

            System.out.println(s); //Hi, My name is Minseok Jeong.
            System.out.println(s.toLowerCase()); //hi, my name is minseok jeong.
      }
}
