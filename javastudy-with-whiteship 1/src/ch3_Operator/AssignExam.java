package ch3_Operator;

public class AssignExam {
      public static void main(String[] args) {
            // =
            Integer num=3;
            String word="apple";

            // +=
            num+=5;
            word+=" mango";
            System.out.println(num); //8
            System.out.println(word); //apple mango
            System.out.println();

            // -=
            num-=2;
            System.out.println(num); //6
            System.out.println();

            // *=
            num*=5;
            System.out.println(num); //30
            System.out.println();

            // /=
            num/=2;
            System.out.println(num); //15
            System.out.println();


      }
}
