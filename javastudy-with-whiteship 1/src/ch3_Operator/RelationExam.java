package ch3_Operator;

public class RelationExam {
      public static void main(String[] args) {
            //==
            System.out.println(1==1); //true
            System.out.println(1==3); //false
            System.out.println();

            // !=
            System.out.println(1!=1); //false
            System.out.println(1!=3); //true
            System.out.println();

            // >
            System.out.println(5>3); //true
            System.out.println(1>3); //false
            System.out.println();

            // >=
            System.out.println(5>=1); //true
            System.out.println(1>=1); //true
            System.out.println(1>=3); //false
            System.out.println();

            // <
            System.out.println(1<3); //true
            System.out.println(5<3); //false
            System.out.println();

            // <=
            System.out.println(1<=3); //true
            System.out.println(1<=1); //true
            System.out.println(3<=1); //false
            System.out.println();

      }
}
